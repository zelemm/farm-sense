<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\SoftDeletes;

class FarmFenceCoordinates extends Model
{
    use HasFactory, SoftDeletes;

    protected $guarded = [];

    public function fence(){
        return $this->belongsTo(FarmFence::class);
    }

    public function scopeFilter($query, array $filters)
    {
        $query->when($filters['search'] ?? null, function ($query, $search) {
            $query->where('farm_fence_coordinates.longitude', 'like', '%'.$search.'%')
                ->where('farm_fence_coordinates.latitude', 'like', '%'.$search.'%');
        })->when($filters['trashed'] ?? null, function ($query, $trashed) {
            if ($trashed === 'with') {
                $query->withTrashed();
            } elseif ($trashed === 'only') {
                $query->onlyTrashed();
            }
        });
    }
}
